# Lists and tuples are both sequence data types in Python. The main differences
#  between them are:

# Lists are mutable, meaning their elements can be changed, while tuples are
# immutable.
# Lists use square brackets `[]`, while tuples use parentheses `()`.
# Lists generally have a variable length, while tuples have a fixed length.

# difference between `==` and `is` in Python

